

function mostrarCuadro() {
    const cuadro = document.getElementById("cuadro1");
    if (cuadro.style.display === "none" || cuadro.style.display === "") {
      cuadro.style.display = "flex";
    } else {
      cuadro.style.display = "none";
    }
  }

 function ocultarCuadro() {
    document.getElementById("cuadro1").style.display = "none";

}

